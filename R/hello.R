#' Function to add two numbers
#'
#' @param a First number
#' @param b Second number
#'
#' @return
#' @export
#'
#' @examples
#' add_two_numbers(2, 4)
#' add_two_numbers(7, -4)
#'
add_two_numbers <- function(a, b) {
  return(a+b)
}

#' Title
#'
#' @param a
#' @param b
#'
#' @return
#' @export
#'
#' @examples
multiply_two_numbers <- function(a, b) {
  return(a*b)
}
