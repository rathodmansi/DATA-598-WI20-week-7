
#' Title: Add two numbers function
#'
#' @param a First Number
#' @param b Second Number
#'
#' @return
#' @export
#'
#' @examples
#' add_two_numbers(2, 4)
#' add_two_numbers(7, -4)
add_two_numbers <- function(a, b) {
  return(a+b)
}

#' Title: Multiply two numbers function
#'
#' @param a First Number
#' @param b Second Number
#'
#' @return
#' @export
#'
#' @examples
#' multiply_two_numbers(2,5)
#' multiply_two_numbers(2,1)
#
multiply_two_numbers <- function(a, b) {
  return(a*b)

}
